#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import {
  AGENT_CONTRACT_LIMITS,
  AGENT_CONTRACT_VERSION,
  AGENT_MARKERS,
  AGENT_OUTCOME_SCHEMA,
  AGENT_RESULT_FORMATS,
  AGENT_RUNTIMES,
  AGENT_SANDBOX_MODES,
} from "../src/shared/agent-contract.ts";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const outputPath = path.join(repoRoot, "scripts", "wmux_agent_contract.py");
const check = process.argv.includes("--check");

const pythonString = (value) => JSON.stringify(value);
const pythonTuple = (values) =>
  `(${values.map(pythonString).join(", ")}${values.length === 1 ? "," : ""})`;
const schemaJson = JSON.stringify(AGENT_OUTCOME_SCHEMA);

const generated = `# Generated by scripts/generate-agent-contract.mjs. Do not edit.
import json as _json

AGENT_CONTRACT_VERSION = ${AGENT_CONTRACT_VERSION}

AGENT_READY = ${pythonString(AGENT_MARKERS.ready)}
AGENT_RESULT = ${pythonString(AGENT_MARKERS.result)}
AGENT_DONE = ${pythonString(AGENT_MARKERS.done)}
AGENT_TUI_READY = ${pythonString(AGENT_MARKERS.tuiReady)}
AGENT_TUI_LAUNCH = ${pythonString(AGENT_MARKERS.tuiLaunch)}
AGENT_TUI_ACK = ${pythonString(AGENT_MARKERS.tuiAck)}
AGENT_TUI_EXIT = ${pythonString(AGENT_MARKERS.tuiExit)}
AGENT_TUI_RELEASE = ${pythonString(AGENT_MARKERS.tuiRelease)}

MAX_LINE = ${AGENT_CONTRACT_LIMITS.maxLine}
MAX_PROMPT = ${AGENT_CONTRACT_LIMITS.maxPrompt}
MAX_TEXT = ${AGENT_CONTRACT_LIMITS.maxText}
TERMINATION_GRACE = ${AGENT_CONTRACT_LIMITS.terminationGraceSeconds}
HELP_PROBE_TIMEOUT = ${AGENT_CONTRACT_LIMITS.helpProbeTimeoutSeconds}
MAX_HELP_OUTPUT = ${AGENT_CONTRACT_LIMITS.maxHelpOutput}
MAX_BLANK_REQUEST_LINES = ${AGENT_CONTRACT_LIMITS.maxBlankRequestLines}
MAX_RUN_ID = ${AGENT_CONTRACT_LIMITS.maxRunId}
MAX_DIRECTORY = ${AGENT_CONTRACT_LIMITS.maxDirectory}
MAX_OPTION_TEXT = ${AGENT_CONTRACT_LIMITS.maxOptionText}

RUNTIMES = frozenset(${pythonTuple(AGENT_RUNTIMES)})
SANDBOX_MODES = frozenset(${pythonTuple(AGENT_SANDBOX_MODES)})
RESULT_FORMATS = frozenset(${pythonTuple(AGENT_RESULT_FORMATS)})
OUTCOME_SCHEMA = _json.loads(${pythonString(schemaJson)})
`;

if (check) {
  const current = fs.existsSync(outputPath)
    ? fs.readFileSync(outputPath, "utf8")
    : "";
  if (current !== generated) {
    console.error(
      "scripts/wmux_agent_contract.py is stale; run npm run generate:contracts",
    );
    process.exit(1);
  }
} else {
  fs.writeFileSync(outputPath, generated);
}
